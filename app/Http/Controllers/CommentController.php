    {
        $comments = Comment::when(request('search'),function($query){
            $search = request('search');
            $query->orWhere('comment','like',"%$search%");
        })->with('user','package')
        ->latest('id')->paginate(10)->withQueryString();
        return view('admin.comment.index',compact('comments'));
    }

